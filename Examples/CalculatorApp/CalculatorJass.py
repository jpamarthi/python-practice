# This method returns addition of two numbers
def add_numbers(a, b):
    return a + b


# This method returns subtraction of two numbers
def subtract_numbers(a, b):
    return a - b


# This method returns division of two numbers
def divide_numbers(a, b):
    return a / b


# This method returns we multiply 2 numbers
def multipli_numbers(a, b):
    return a * b


# This method returns remainder when two numbers are divided
def exponentiation_number(a, b):
    return a ** b