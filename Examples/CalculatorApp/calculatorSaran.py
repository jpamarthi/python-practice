# This Method Adds Two values
def add_numbers(a, b):
    return a + b


# This Method Divides Two values
def divide_numbers(a, b):
    return a / b


# This Method Subtracts Two values
def subtract_numbers(a, b):
    return a - b


# This Method Multiply Two values
def multipli_numbers(a, b):
    return a * b


# This Method Returns the Reminder when we do division for Two values
def modulo_numbers(a, b):
    return a % b

